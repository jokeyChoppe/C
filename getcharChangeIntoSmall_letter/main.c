# include <stdio.h>

int main()
{
    char c1, c2;
    c1 = getchar();
    c2 =c1 + 32;
    putchar(c2);
    putchar('\n');
    printf("大写字母：%c\n小写字母：%c\n", c1, c2);

    return 0;
}

